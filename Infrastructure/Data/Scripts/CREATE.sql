CREATE DATABASE IF NOT EXISTS UserDatabase;
use UserDatabase;

CREATE TABLE IF NOT EXISTS Users (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    BirthdayDate VARCHAR(20),
    Password VARCHAR(255) NOT NULL,
    PhoneNumber VARCHAR(20),
    Email VARCHAR(150) NOT NULL UNIQUE,
    IsLeader BOOLEAN NOT NULL DEFAULT FALSE,
    CreatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE TeamsAndFunctions (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Teams VARCHAR(100) NOT NULL,
    Functions VARCHAR(100) NOT NULL
);

CREATE TABLE UserTeams (
    UserId INT NOT NULL,
    TeamId INT NOT NULL,
    PRIMARY KEY (UserId, TeamId),
    FOREIGN KEY (UserId) REFERENCES Users(Id),
    FOREIGN KEY (TeamId) REFERENCES TeamsAndFunctions(Id)
);


